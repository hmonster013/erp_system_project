    private void jButton_XoaBHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_XoaBHMouseClicked
       // Lấy mã nhân viên và mã bảo hiểm được chọn từ combo box
    String selectedNhanVien = (String) jComboBox_MaNhanVien.getSelectedItem();
    String selectedBaoHiem = (String) jComboBox_mabaohiem.getSelectedItem();

    // Tách chuỗi để lấy phần mã (phần trước dấu '-')
    String maNV = selectedNhanVien.split(" - ")[0];
    String maBH = selectedBaoHiem.split(" - ")[0];

    // Xác nhận người dùng có chắc chắn muốn xóa
    int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa thông tin bảo hiểm này?", "Xác nhận", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        // Gọi phương thức xóa bảo hiểm trong Controller
        boolean result = quanLyBaoHiemController.deleteBaoHiem(maNV, maBH);

        if (result) {
            JOptionPane.showMessageDialog(this, "Xóa thông tin bảo hiểm thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Xóa thông tin bảo hiểm thất bại!");
        }

        // Làm mới bảng dữ liệu
        quanLyBaoHiemController.refreshDataTable();
    }
    }//GEN-LAST:event_jButton_XoaBHMouseClicked
    
      
