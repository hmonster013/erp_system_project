    private void addDataToComboBox() {
        List<Object[]> danhSachNhanVien = quanLyHoSoModel.danhSachHoSoNV();
        
        jComboBox_MaNhanVien.removeAllItems();
        
        for (Object[] nhanVien : danhSachNhanVien) {
            String tenNhanVien = nhanVien[0].toString() + " - " + nhanVien[1].toString();
            jComboBox_MaNhanVien.addItem(tenNhanVien);
        }
    }
    
