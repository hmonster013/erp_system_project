    private void jTable_HopDongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_HopDongMouseClicked
//       // Lấy chỉ số hàng được chọn
    int selectedRow = jTable_HopDong.getSelectedRow();

    // Kiểm tra xem có hàng nào được chọn không
    if (selectedRow != -1) {
        // Lấy dữ liệu từ hàng được chọn
        String maHD = jTable_HopDong.getValueAt(selectedRow, 0).toString(); // Mã hợp đồng
        String tenHD = jTable_HopDong.getValueAt(selectedRow, 1).toString(); // Tên hợp đồng
        String maNV = jTable_HopDong.getValueAt(selectedRow, 2).toString(); // Mã nhân viên
        Date ngayBatDau = (Date) jTable_HopDong.getValueAt(selectedRow, 3); // Ngày bắt đầu
        Date ngayKetThuc = (Date) jTable_HopDong.getValueAt(selectedRow, 4); // Ngày kết thúc
        String vanBan = jTable_HopDong.getValueAt(selectedRow, 5).toString(); // Văn bản

        // Cập nhật các trường văn bản với dữ liệu đã chọn
        setMHD(maHD);
        setTenHD(tenHD);
        setMNV(maNV);
        setStartDate(ngayBatDau);
        setEndDate(ngayKetThuc);
        setVanBan(vanBan);
    }
    }//GEN-LAST:event_jTable_HopDongMouseClicked

