     private void btnLoginActionPerformed() {
        String username = txtusername.getText();
        String password = new String(txtpassword.getPassword());

        // Kiểm tra xem username hoặc password có bị để trống không
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin đăng nhập!", "Thiếu thông tin", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        LoginController loginController = new LoginController();

        try {
            if (loginController.login(username, password)) {
                // Đăng nhập thành công
                JOptionPane.showMessageDialog(this, "Đăng nhập thành công!");
                
                openSubsystemHome(loginController.getMaBoPhan(username));
            } else {
                // Đăng nhập thất bại
                JOptionPane.showMessageDialog(this, "Sai tên đăng nhập hoặc mật khẩu!", "Lỗi đăng nhập", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi trong quá trình đăng nhập. Vui lòng thử lại sau!", "Lỗi hệ thống", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    
